package net.dept.db;

public class Dept {
	private int d_num;
	private String d_name;
	private int d_level;
	private String d_color;
	private int d_upperlevel;

	public int getD_num() {
		return d_num;
	}

	public void setD_num(int d_num) {
		this.d_num = d_num;
	}

	public String getD_name() {
		return d_name;
	}

	public void setD_name(String d_name) {
		this.d_name = d_name;
	}

	public int getD_level() {
		return d_level;
	}

	public void setD_level(int d_level) {
		this.d_level = d_level;
	}

	public String getD_color() {
		return d_color;
	}

	public void setD_color(String d_color) {
		this.d_color = d_color;
	}

	public int getD_upperlevel() {
		return d_upperlevel;
	}

	public void setD_upperlevel(int d_upperlevel) {
		this.d_upperlevel = d_upperlevel;
	}

}
