package net.approval.db;

public class ApprovalTemplate {
	private int a_template_num;
	private String a_template_name;
	private String a_template_content;

	public int getA_template_num() {
		return a_template_num;
	}

	public void setA_template_num(int a_template_num) {
		this.a_template_num = a_template_num;
	}

	public String getA_template_name() {
		return a_template_name;
	}

	public void setA_template_name(String a_template_name) {
		this.a_template_name = a_template_name;
	}

	public String getA_template_content() {
		return a_template_content;
	}

	public void setA_template_content(String a_template_content) {
		this.a_template_content = a_template_content;
	}

}
